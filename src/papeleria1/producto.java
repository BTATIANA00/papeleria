
package papeleria1;


public class producto {
    int id;
    String nombre;
    double cantidad;
    double precio;
    double xcantidad;

    public producto() {
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public double getCantidad() {
        return cantidad;
    }

    public double getPrecio() {
        return precio;
    }

    public double getXcantidad() {
        return xcantidad;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setCantidad(double cantidad) {
        this.cantidad = cantidad;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public void setXcantidad(double xcantidad) {
        this.xcantidad = xcantidad;
    }

    

    
}
